def format_history(promotions: list) -> str:
    """昇進履歴を時系列文字列に整形: 2014/6→係長 ／ 2016/6→課長 ..."""
    parts = [f'{y}/{m}→{r}' for y, m, r in sorted(promotions)]
    return ' ／ '.join(parts)


def main():
    service = get_sheets_service()

    # ---------- 1. 現在の氏名列を取得 ----------
    result = service.spreadsheets().values().get(
        spreadsheetId=SPREADSHEET_ID,
        range='シート1!A:A'
    ).execute()
    name_rows = result.get('values', [])
    print(f'既存行数: {len(name_rows)}')

    # ---------- 2. 列Eを挿入（D=役職 の直後） ----------
    service.spreadsheets().batchUpdate(
        spreadsheetId=SPREADSHEET_ID,
        body={
            'requests': [{
                'insertDimension': {
                    'range': {
                        'sheetId': SHEET_ID,
                        'dimension': 'COLUMNS',
                        'startIndex': INSERT_COL_INDEX,
                        'endIndex': INSERT_COL_INDEX + 1,
                    },
                    'inheritFromBefore': True
                }
            }]
        }
    ).execute()
    print('列E を挿入しました（D=役職 の右隣）')

    # ---------- 3. ヘッダーとデータを書き込む ----------
    data_updates = [
        # 行1, 行2: 空（既存ヘッダー行に合わせる）
        {'range': 'シート1!E1', 'values': [['']]},
        {'range': 'シート1!E2', 'values': [['']]},
        # 行3: 列ヘッダー
        {'range': 'シート1!E3', 'values': [['昇進履歴']]},
    ]

    written = 0
    not_found = []
    for i, row in enumerate(name_rows):
        row_num = i + 1
        if row_num <= 3:
            continue  # ヘッダー行はスキップ
        if not row or not row[0].strip():
            continue
        name = row[0].strip()
        if name in PROMOTION_HISTORY:
            hist_str = format_history(PROMOTION_HISTORY[name])
            data_updates.append({
                'range': f'シート1!E{row_num}',
                'values': [[hist_str]]
            })
            written += 1
            print(f'  ✓ {name}: {hist_str}')
        else:
            not_found.append(name)

    # バッチ書き込み
    service.spreadsheets().values().batchUpdate(
        spreadsheetId=SPREADSHEET_ID,
        body={
            'valueInputOption': 'USER_ENTERED',
            'data': data_updates
        }
    ).execute()

    print(f'\n=== 完了 ===')
    print(f'昇進履歴を記入した社員: {written} 名')
    print(f'記録なし（75期以前に昇格済み、または現在役職なし）: {len(not_found)} 名')
    if not_found:
        print('  記録なし:', ', '.join(not_found))


if __name__ == '__main__':
    main()
